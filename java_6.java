//Lecture 6 in which we will learn about Operators

public class java_6 {
    public static void main(String[] args) {
        int a = 8;
        int b = 3;
        int sum = a + b;
        int sub = a - b;
        int mul = a * b;
        int div = a / b;

        System.out.println(sum);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
    }
}